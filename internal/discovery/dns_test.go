func TestDNSProbeResolvConf(t *testing.T) {
	resolvConf := `
# Generated by NetworkManager
nameserver 1.1.1.1
search corp.example.com example.com
domain legacy.example.com
nameserver 8.8.8.8
`
	path := writeTempFile(t, resolvConf)

	var queried []string
	probe := discovery.DNSProbe(discovery.DNSOptions{
		ResolvConf: path,
		LookupTXT: func(_ context.Context, name string) ([]string, error) {
			queried = append(queried, name)
			return nil, errors.New("not found")
		},
	})

	_, err := probe(context.Background())
	require.NoError(t, err)

	assert.Contains(t, queried, "kcompass.corp.example.com")
	assert.Contains(t, queried, "kcompass.example.com")
	assert.Contains(t, queried, "kcompass.legacy.example.com")
}

