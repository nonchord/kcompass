// --- parseTXTRecord edge cases via probe ---

func TestParseTXTRecordValid(t *testing.T) {
	cases := []string{
		"v=kc1; backend=https://github.com/company/clusters",
		"v=kc1;backend=git@github.com:company/clusters",
		"v=kc1 ; backend=https://github.com/company/clusters",
	}
	for _, input := range cases {
		t.Run(input, func(t *testing.T) {
			probe := discovery.DNSProbe(discovery.DNSOptions{
				SearchDomains: []string{"x.com"},
				LookupTXT: func(_ context.Context, _ string) ([]string, error) {
					return []string{input}, nil
				},
			})
			b, err := probe(context.Background())
			require.NoError(t, err)
			assert.NotNil(t, b, "expected backend for %q", input)
		})
	}
}

func TestParseTXTRecordInvalid(t *testing.T) {
	cases := []string{
		"",
		"v=spf1",
		"v=kc1",           // no backend field
		"v=kc1; backend=", // empty URL
		"backend=https://github.com/company/clusters", // missing v=kc1
	}
	for _, input := range cases {
		t.Run(input, func(t *testing.T) {
			probe := discovery.DNSProbe(discovery.DNSOptions{
				SearchDomains: []string{"x.com"},
				LookupTXT: func(_ context.Context, _ string) ([]string, error) {
					return []string{input}, nil
				},
			})
			b, err := probe(context.Background())
			assert.NoError(t, err)
			assert.Nil(t, b, "expected nil backend for %q", input)
		})
	}
}

func writeTempFile(t *testing.T, content string) string {
	t.Helper()
	f, err := os.CreateTemp(t.TempDir(), "resolv*.conf")
	require.NoError(t, err)
	_, err = f.WriteString(content)
	require.NoError(t, err)
	require.NoError(t, f.Close())
	return f.Name()
}
