// mockTXT returns a LookupTXT function that maps hostnames to TXT record slices.
func mockTXT(records map[string][]string) func(ctx context.Context, name string) ([]string, error) {
	return func(_ context.Context, name string) ([]string, error) {
		if recs, ok := records[name]; ok {
			return recs, nil
		}
		return nil, errors.New("no records")
	}
}

func TestDNSProbeFound(t *testing.T) {
	probe := discovery.DNSProbe(discovery.DNSOptions{
		SearchDomains: []string{"internal.company.com"},
		LookupTXT: mockTXT(map[string][]string{
			"kcompass.internal.company.com": {"v=kc1; backend=https://github.com/company/clusters"},
		}),
	})
	b, err := probe(context.Background())
	require.NoError(t, err)
	require.NotNil(t, b)
	assert.Contains(t, b.Name(), "github.com")
}

